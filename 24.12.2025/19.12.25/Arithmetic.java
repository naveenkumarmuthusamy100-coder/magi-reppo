class Arithmetic
{
	public static void main(String[] args)
	{
		int a=20;
		int b=6;
		
		int add=a+b;
		int sub=a-b;
		int mul=a*b;
		int div=a/b;
		int rem=a%b;
	
	System.out.println(add);
	System.out.println(sub);
	System.out.println(mul);
	System.out.println(div);	
	System.out.println(rem);
	}
}

